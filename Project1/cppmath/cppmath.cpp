#include <iostream>
#include <cmath> // log, sqrt, round kullanabilmek için gerekli
using namespace std;
int main()
{
	cout << max(7, 8)<<endl; 
	cout << min(9, 4)<<endl; 
	cout << sqrt(225)<<endl; // karekök 
	cout << round(9.87)<<endl; // en yakın tam sayıya yuvarlar
	cout << log(10);
	return 0;
}
